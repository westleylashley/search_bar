class TableViewController: UITableViewController, UISearchBarDelegate {
    
    @IBOutlet weak var searchBar: UISearchBar!

// MARK: - Test Data
    let data = ["New York, NY", "Los Angeles, CA", "Chicago, IL", "Houston, TX",
